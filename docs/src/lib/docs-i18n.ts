export const DOCS_TRANSLATIONS: Record<string, T | undefined> = { en: undefined, es, fr, de, pt };

/** Normalize an arbitrary cookie value to a supported locale (defaults to 'en'). */
export function normalizeLocale(code: string | undefined): string {
  return code && code in DOCS_TRANSLATIONS ? code : 'en';
}
